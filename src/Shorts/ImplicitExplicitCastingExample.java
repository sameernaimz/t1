package Shorts;

public class ImplicitExplicitCastingExample {

    public static void main(String[] args) {

        // implicit casting

        byte a = 100;
        int b = a;


        //explicit casting

        double c = 3.14;
        int i = (int ) c;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(i);
    }
}
