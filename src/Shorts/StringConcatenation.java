package Shorts;

public class StringConcatenation {
    public static void main(String[] args) {

        String name = "Loop Academy";
        String word = " Welcome to ";
        String word2 = "Duration of the course is ";
        int month = 6;
        String word3 = "Months";

        System.out.println(word + name);
        System.out.println("Loop Academy " + "will help you to get into IT");
        System.out.println(word2 + month + word3);
    }
}
