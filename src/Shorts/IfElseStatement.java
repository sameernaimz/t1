package Shorts;

public class IfElseStatement {
    public static void main(String[] args) {
        int mySpeed = 40;
        int speedLimit = 50;

        if (mySpeed <= speedLimit) {
            System.out.println("You good, keep driving");
        } else {
            System.out.println("You are driving too fast, slow down");
        }
    }
}
